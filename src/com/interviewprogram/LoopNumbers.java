package com.interviewprogram;

public class LoopNumbers {
	
	public static void main(String[] args) {
		
		
		for(int i =1;i<=2;i++) {
			//int i=1;1<=2;i=1
			//int i=2;2<=2;i=2
			
			for(int j=1;j<=2;j++) {
				//int j=1;1<=2;j=1
				//int j=2;2<=2;j=2
				
				//int j=1 1<=2 j=1
				//int j=2 2<=2 j=2
				
				System.out.println(j);
			}
		}
		//System.out.println();
	}

}
